using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Parcial_1_variante_A_Serie_II_ejeR1
{
    internal class Calculadora
    {
        public int suma(int x1, int x2)
        {
            return x1 + x2;
        }

        public int resta(int x1, int x2)
        {
            return x1 - x2;
        }

        public int Multiplicacion(int x1, int x2)
        {
            return x1 * x2;
        }

        public int Division(int x1, int x2)
        {
            return x1 / x2;
        }
    }
}
